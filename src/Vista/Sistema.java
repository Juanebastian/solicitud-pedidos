    private void btnAgregarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarClienteActionPerformed
        // TODO add your handling code here:
     if (!txtIdCliente.getText().isEmpty() && !txtNombreCliente.getText().isEmpty() && !txtDocCliente.getText().isEmpty() ) {
            
    try {
        int id = Integer.parseInt(txtIdCliente.getText());
        String nombre = txtNombreCliente.getText();
        int doc = Integer.parseInt(txtDocCliente.getText());
        int menu1 = Integer.parseInt(txtCantMenu1.getText());
        int menu2 = Integer.parseInt(txtCantMenu2.getText());
        int menu3 = Integer.parseInt(txtCantMenu3.getText());
        int menu4 = Integer.parseInt(txtCantMenu4.getText());
        
        
        int total =  (menu1 * 20000) + (menu2 * 25000) + (menu3 * 28000) +(menu4 * 30000)  ;
        
        
        cl.setId(id);
        cl.setNombres(nombre);
        cl.setDoc(doc);
        cl.setMenu_1(menu1);
        cl.setMenu_2(menu2);
        cl.setMenu_3(menu3);
        cl.setMenu_4(menu4);
        cl.setTotal(total);
        
        client.AgregarCliente(cl);
        
        
        JOptionPane.showMessageDialog(null, "Solicitud registrada");
        ListarCliente();
        LimpiarTable();
        LimpiarCliente();
        ListarCliente();
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Uno o más campos no son números válidos");
    }
} else {
    JOptionPane.showMessageDialog(null, "Uno o más campos están vacíos");
}
    }//GEN-LAST:event_btnAgregarClienteActionPerformed

    private void btnEliminarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarClienteActionPerformed
        // TODO add your handling code here:

        if (!"".equals(txtIdCliente.getText())) {
            int pregunta = JOptionPane.showConfirmDialog(null, "Esta seguro de eliminar la solicitud?");
            if (pregunta == 0) {
                int id = Integer.parseInt(txtIdCliente.getText());
                client.EliminarCliente(id);
                LimpiarTable();
                LimpiarCliente();
                ListarCliente();
            }
        }
    }//GEN-LAST:event_btnEliminarClienteActionPerformed

    private void btnActualizarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarClienteActionPerformed
        // TODO add your handling code here:

        if ("".equals(txtIdCliente.getText())) {
            JOptionPane.showMessageDialog(null, "Seleccione una fila");
        } else {

            if (!"".equals(txtDocCliente.getText()) || !"".equals(txtIdCliente.getText()) || !"".equals(txtNombreCliente.getText()) || !"".equals(txtDocCliente.getText()) || !"".equals(txtCantMenu1.getText()) || !"".equals(txtCantMenu1.getText())) {
           
                
            cl.setId(Integer.parseInt(txtIdCliente.getText()));
            
            cl.setNombres(txtNombreCliente.getText());
            
            cl.setDoc(Integer.parseInt(txtDocCliente.getText()));
            
            
            
            cl.setMenu_1(Integer.parseInt(txtCantMenu1.getText()));
            
            cl.setMenu_2(Integer.parseInt(txtCantMenu2.getText()));
            
            cl.setMenu_3(Integer.parseInt(txtCantMenu3.getText()));
            
            cl.setMenu_4(Integer.parseInt(txtCantMenu4.getText()));
            
                client.ActualizarCliente(cl);
                
                JOptionPane.showMessageDialog(null, "Solicitud Modificada");
                LimpiarTable();
                LimpiarCliente();
                ListarCliente();
            } else {
                JOptionPane.showMessageDialog(null, "The fields are empty");
            }
        }
    }//GEN-LAST:event_btnActualizarClienteActionPerformed

