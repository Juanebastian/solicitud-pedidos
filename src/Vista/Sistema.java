    public void ListarCliente() {
        List<Cliente> ListarCl = client.ListarCliente();
        modelo = (DefaultTableModel) TableCliente.getModel();
        Object[] ob = new Object[8];
        for (int i = 0; i < ListarCl.size(); i++) {
            ob[0] = ListarCl.get(i).getId();
            ob[1] = ListarCl.get(i).getNombres();
            ob[2] = ListarCl.get(i).getDoc();
            ob[3] = ListarCl.get(i).getMenu_1();
            ob[4] = ListarCl.get(i).getMenu_2();
            ob[5] = ListarCl.get(i).getMenu_3();
            ob[6] = ListarCl.get(i).getMenu_4();
            ob[7] = ListarCl.get(i).getTotal();
            modelo.addRow(ob);
        }
        TableCliente.setModel(modelo);

    }

    

    public void LimpiarTable() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }

    

    

    
    

